package rtree.log;

/**
 * Enum representing different log levels.
 * 
 * DEBUG: Detailed information for debugging purposes.
 * WARN: Warning events
 * ERROR: Error events 
 */
public enum LogLevel {
	/**
	 * Detailed information for debugging
	 */
	DEBUG,
	
	/**
	 * Warning events
	 */
	WARN,
	
	/**
	 * Error events
	 */
	ERROR
}